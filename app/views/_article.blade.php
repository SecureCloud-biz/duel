      <h1>Workers stage massive rallies for pay rise</h1>
      <div><img src="http://www.thetimes.co.uk/tto/multimedia/archive/00788/165046797_Marchingo_788474c.jpg" width="100%"></div>
      <div class="caption">The TUC estimated 80,000 to 90,000 people were on the march in central London</div>
      <div class="byline">David Rankin</div>
      <div class="update">Updated 12:20pm Oct 18</div>
  
      <div>
        <p>Tens of thousands of workers have joined protests today demanding a pay increase.</p>
        <p>The TUC has organised protests in London, Glasgow and Belfast under the banner Britain Needs a Pay Rise.</p>
        <p>Midwives went on strike for the first time in their history this week, following the government’s decision not to pay a recommended 1% increase to all NHS staff. Hospital radiographers and prison officers will take strike action next week in the same dispute. </p>
        <p>TUC general secretary Frances O’Grady said: “Today tens of thousands of people will join the TUC’s Britain Needs a Pay Rise march.</p>
        <p>“Our message is that after the longest and deepest pay squeeze in recorded history, it’s time to end the lock-out that has kept the vast majority from sharing in the economic recovery.</p>
        <p>“The average worker is £50-a-week worse off than in 2007 and 5m earn less than the living wage. Meanwhile, top directors now earn 175 times more than the average worker.</p>
        <p>
        “If politicians wonder why so many feel excluded from the democratic process, 
        they should start with bread and butter living standards. 
        </p>
        <p>
        “An economy that finds money for tax cuts for the rich and boardroom greed, 
        while the rest face a pay squeeze and big cuts to the welfare system - that 
        any of us might need - is no longer working for the many.” 
        </p>
        <p>
        Len McCluskey, general secretary of Unite, urged Labour to offer a “clear 
        socialist alternative” at the next election.
        </p><p>
        He said: “I say to Labour - stop being scared of your own shadow. Don’t shrink 
        what you offer the British people.
        </p><p>
        “The time for being timid is past. Be brave, be inspired by this march today.
        </p><p>
        “Believe that people power and working class movements can change our country 
        for the better.”
        </p><p>
        Dave Prentis, general secretary of Unison, said 600 public sector jobs had 
        been lost every day since the coalition came to power, adding: “We have a 
        story of two nations - one where champagne corks are popping for the bankers 
        and boardroom pay is soaring, while in the other world our people are 
        suffering from poverty pay.”
        </p><p>
        Cathy Warwick, chief executive of the Royal College of Midwives, was 
        “overwhelmed” by the strong response from midwives in England to the 
        industrial action. 
        </p><p>
        She added: “It shows the depth of feeling on this and the levels of anger and 
        discontent among our members. I really encourage those that can make it to 
        join us on Saturday in London, Belfast and Glasgow so that we can show the 
        Government just how strong their feelings are and how much support there is 
        for fair pay. 
        </p><p>
        “For too long our members and their fellow workers have suffered pay freezes 
        with income rapidly falling behind the real cost of living. They are not 
        asking to be paid like bankers, just for a fair reward for the work they do. 
        Enough’s enough and now is the time to take a stand.” 
        </p><p>
        GMB union general secretary Paul Kenny said: “People are currently facing the 
        biggest squeeze on their incomes since Victorian times, and wages have 
        fallen in real terms every year since 2010. 
        </p><p>
        “The economic recovery has a long way to go as even with strong growth this 
        year GDP per head will be still 3% below 2007 levels. This is the root cause 
        of the value of earnings being down by 14% in real terms. 
        </p><p>
        “Living standards of GMB members continue to decline, because of increased 
        housing costs, childcare, food and utility bills. Members have faced imposed 
        pay freezes, or derisory pay offers which do not bring earnings up to the 
        levels that were lost during this long-running recession.” 
        </p><p>
        Marchers held up giant Union balloons and blew horns and whistles, giving a 
        carnival atmosphere to the protest. 
        </p><p>
        A band played at the head of the London march, followed by stilt walkers. 
        </p><p>
        The march was going past Westminster ahead of a rally in Hyde Park which was 
        being addressed by union leaders. 
        </p>
